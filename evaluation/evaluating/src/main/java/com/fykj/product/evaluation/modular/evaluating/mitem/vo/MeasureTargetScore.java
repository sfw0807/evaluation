package com.fykj.product.evaluation.modular.evaluating.mitem.vo;

import com.fykj.platform.kernel._c.model.JModel;

public class MeasureTargetScore implements JModel{

	private String evalTargetId;
	
	private String name;
	
	private String wjScore;
	
	private String tbScore;
	
	private String itemName;
	
	private String sumScore;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWjScore() {
		return wjScore;
	}

	public void setWjScore(String wjScore) {
		this.wjScore = wjScore;
	}

	public String getTbScore() {
		return tbScore;
	}

	public void setTbScore(String tbScore) {
		this.tbScore = tbScore;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getSumScore() {
		return sumScore;
	}

	public void setSumScore(String sumScore) {
		this.sumScore = sumScore;
	}

	public String getEvalTargetId() {
		return evalTargetId;
	}

	public void setEvalTargetId(String evalTargetId) {
		this.evalTargetId = evalTargetId;
	}
	
}
