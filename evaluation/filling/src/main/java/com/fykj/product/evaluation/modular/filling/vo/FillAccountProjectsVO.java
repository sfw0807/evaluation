import com.fykj.product.evaluation.modular.filling.model.FillProject;

import java.util.List;

/**
 * Created by liwang on 2017/4/12.
 */
public class FillAccountProjectsVO {
    private FillAccount fillAccount;
    private List<FillProject> fillProjectList;

    public FillAccountProjectsVO(FillAccount fillAccount, List<FillProject> fillProjectList) {
        this.fillAccount = fillAccount;
        this.fillProjectList = fillProjectList;
    }

    public FillAccount getFillAccount() {
        return fillAccount;
    }

    public void setFillAccount(FillAccount fillAccount) {
        this.fillAccount = fillAccount;
    }

    public List<FillProject> getFillProjectList() {
        return fillProjectList;
    }

    public void setFillProjectList(List<FillProject> fillProjectList) {
        this.fillProjectList = fillProjectList;
    }
}
